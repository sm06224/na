/* ============================================================
   切り絵 — 影絵の主役たち。局所座標で、丈はおよそ 1、中心は原点。
   x は右、y は下（画面と同じ向き）。各「型」は部品（閉じた多角形）の
   集まりで、影は部品ごとに落ちて、壁の上で重なる。

   依存ゼロ。丸い縁は、読み込み時に決定的に点を撒いて象る。
   ============================================================ */

/* 円周上に n 点を撒く。 */
function circle(cx, cy, r, n = 36, fn = null) {
  const pts = [];
  for (let i = 0; i < n; i++) {
    const t = (i / n) * Math.PI * 2;
    const rr = fn ? r * fn(t) : r;
    pts.push([cx + Math.cos(t) * rr, cy + Math.sin(t) * rr]);
  }
  return pts;
}

/* 二つの円の差で三日月を象る（右に欠ける月）。 */
function crescent(r0 = 0.5, d = 0.2, r1 = 0.46, n = 40) {
  // 外円 C0=(0,0,r0) と 内円 C1=(d,0,r1) の交点（角）を求める。
  const x = (d * d + r0 * r0 - r1 * r1) / (2 * d);
  const y = Math.sqrt(Math.max(0, r0 * r0 - x * x));
  const aOut = Math.atan2(y, x);          // 外円側の角（上の角）
  const aIn = Math.atan2(y, x - d);       // 内円側の角
  const pts = [];
  // 外円を、上の角から左回りに大きく回って下の角まで（左の膨らみ）。
  for (let i = 0; i <= n; i++) {
    const t = aOut + (i / n) * (2 * Math.PI - 2 * aOut);
    pts.push([Math.cos(t) * r0, Math.sin(t) * r0]);
  }
  // 内円を、下の角から上の角へ戻る（欠けの輪郭）。
  for (let i = 0; i <= n; i++) {
    const t = -aIn + (i / n) * (2 * aIn);
    pts.push([d + Math.cos(t) * r1, Math.sin(t) * r1]);
  }
  return pts;
}

