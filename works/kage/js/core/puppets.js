/* ============================================================
   切り絵 — 影絵の主役たち。いまや「関節」を持つ。

   各「型」は手足（limb）の集まり。手足は閉じた多角形 points と、
   （動くものは）回転の支点 pivot、揺れ方 swing を持つ。worldParts が
   時刻とともに支点まわりに手足を揺らし、全体がそっと息をする——
   だから影は、平たい紙ではなく、生き物に見える。

   局所座標で、丈はおよそ 1、中心は原点。x は右、y は下（画面と同じ）。
   依存ゼロ。丸い縁は読み込み時に決定的に点を撒いて象る。
   swing = { amp（ラジアン）, hz（毎秒の往復）, phase, wind }。
   wind:true の手足は、触れられなくても（風・波で）いつも揺れる。
   ============================================================ */

function circle(cx, cy, r, n = 36, fn = null) {
  const pts = [];
  for (let i = 0; i < n; i++) {
    const t = (i / n) * Math.PI * 2;
    const rr = fn ? r * fn(t) : r;
    pts.push([cx + Math.cos(t) * rr, cy + Math.sin(t) * rr]);
  }
  return pts;
}

function crescent(r0 = 0.5, d = 0.2, r1 = 0.46, n = 40) {
  const x = (d * d + r0 * r0 - r1 * r1) / (2 * d);
  const y = Math.sqrt(Math.max(0, r0 * r0 - x * x));
  const aOut = Math.atan2(y, x);
  const aIn = Math.atan2(y, x - d);
  const pts = [];
  for (let i = 0; i <= n; i++) {
    const t = aOut + (i / n) * (2 * Math.PI - 2 * aOut);
    pts.push([Math.cos(t) * r0, Math.sin(t) * r0]);
  }
  for (let i = 0; i <= n; i++) {
    const t = -aIn + (i / n) * (2 * aIn);
    pts.push([d + Math.cos(t) * r1, Math.sin(t) * r1]);
  }
  return pts;
}

