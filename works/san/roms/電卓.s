; ============================================================
; 電卓 — シリアルの口でしゃべる、ことば数の少ない計算係。
;
; なにか：打鍵（KEY）から一字ずつ読む計算機。'0'〜'9' で数を
;         組み立て、'+' '-' '*' '/' で演算をつなぎ、'=' で答えを
;         十進でシリアル（OUT）に書く。負の答えは '-' を先に出す。
;         'c' ですべて忘れる。起きたらまず '> ' と促す。
; あそびかた：キーボードで「12+34=」のように打つ。
;         画面には、いま手の中にある数が 2 進の帯で灯る——
;         立った桁は刈安（色12）、寝た桁は鈍色（色2）。
;         左端が最上位。打つたびに帯が変わるのを見るとよい。
;
; たくらみ：割り算だけは零で割る無茶を黙って聞き流す
; （この機械では零で割ると故障してしまうから）。
; 鍵がなにも来ないフレームも、帯を描いて FLIP は欠かさない。
; ============================================================

.const 画面, 0x8000      ; VRAM の先頭
.const 見せろ, 0xF000    ; FLIP
.const 鍵, 0xF005        ; 打鍵キュー（空なら 0）
.const 口, 0xF006        ; シリアル出力

; ---- 起きたら、まず促す ----
  LDI r0, 口
  LDI r1, '>'
  ST [r0], r1
  LDI r1, ' '
  ST [r0], r1

; ---- 毎フレーム：鍵を聞けるだけ聞き、帯を描き、見せる ----
主:
  LDI r6, 0              ; r6 は常に 0 — 変数の台
鍵読み:
  LDI r0, 鍵
  LD r1, [r0]
  CMPI r1, 0
  JZ 打鍵終わり          ; 今フレームの鍵は聞き終えた

  ; ---- 数字：入力 = 入力*10 + 桁 ----
  CMPI r1, '0'
  JLT 数字でない
  CMPI r1, '9'
  JGT 数字でない
  SUBI r1, '0'
  LD r0, [r6+入力]
  LDI r2, 10
  MUL r0, r2
  ADD r0, r1
  ST [r6+入力], r0
  ST [r6+見せ], r0       ; 帯にはいま打っている数を
  JMP 鍵読み
数字でない:
  CMPI r1, '+'
  JZ 演算子
  CMPI r1, '-'
  JZ 演算子
  CMPI r1, '*'
  JZ 演算子
  CMPI r1, '/'
  JZ 演算子
  CMPI r1, '='
  JZ 等号
  CMPI r1, 'c'
  JZ 払う
  JMP 鍵読み             ; 知らない字は聞き流す

; ---- 演算子：先に約束していた計算を済ませ、次の約束を覚える ----
演算子:
  PUSH r1
  CALL 適用
  POP r1
  ST [r6+演算], r1
  LDI r0, 0
  ST [r6+入力], r0
  LD r0, [r6+累積]
  ST [r6+見せ], r0       ; 帯には途中の答えを
  JMP 鍵読み

; ---- 等号：締めて、答えを告げ、また白紙から ----
等号:
  CALL 適用
  CALL 答え告げ
  LD r0, [r6+累積]
  ST [r6+見せ], r0       ; 帯には答えを残しておく
  LDI r0, 0
  ST [r6+累積], r0
  ST [r6+入力], r0
  ST [r6+演算], r0
  JMP 鍵読み

; ---- 払う：そろばんを傾けて、ぜんぶ落とす ----
払う:
  LDI r0, 0
  ST [r6+累積], r0
  ST [r6+入力], r0
  ST [r6+演算], r0
  ST [r6+見せ], r0
  JMP 鍵読み

打鍵終わり:
  CALL 帯描き
  CALL 見せる
  JMP 主

; ---- 適用：累積 ← 累積（演算）入力。演算が無ければ 累積 ← 入力 ----
適用:
  LDI r3, 0
  LD r0, [r3+累積]
  LD r1, [r3+入力]
  LD r2, [r3+演算]
  CMPI r2, 0
  JZ 写すだけ
  CMPI r2, '+'
  JZ 足す
  CMPI r2, '-'
  JZ 引く
  CMPI r2, '*'
  JZ 掛ける
  ; 割る。零で割る無茶だけは黙って聞き流す
  CMPI r1, 0
  JZ 適用終わり
  DIV r0, r1
  JMP 記す
足す:
  ADD r0, r1
  JMP 記す
引く:
  SUB r0, r1
  JMP 記す
掛ける:
  MUL r0, r1
  JMP 記す
写すだけ:
  MOV r0, r1
記す:
  ST [r3+累積], r0
適用終わり:
  RET

; ---- 答え告げ：累積を十進で口へ。負なら '-' を先に ----
答え告げ:
  LDI r1, 0
  LD r0, [r1+累積]
  LDI r1, 0x8000
  AND r1, r0
  JZ 正の数
  LDI r1, 口
  LDI r2, '-'
  ST [r1], r2
  LDI r1, 0
  SUB r1, r0             ; 符号を返して大きさだけに
  MOV r0, r1
正の数:
  CALL 十進
  LDI r0, 口
  LDI r1, '\n'
  ST [r0], r1
  LDI r1, '>'
  ST [r0], r1
  LDI r1, ' '
  ST [r0], r1
  RET

; ---- 十進：r0 の値を十進の字で口へ。r0, r1 を壊す ----
十進:
  PUSH r4
  PUSH r5
  LDI r4, 0              ; 積んだ桁の数
  LDI r5, 10
十進割り:
  MOV r1, r0
  MOD r1, r5
  ADDI r1, '0'
  PUSH r1
  ADDI r4, 1
  DIV r0, r5
  CMPI r0, 0
  JNZ 十進割り
十進吐き:
  POP r1
  LDI r0, 口
  ST [r0], r1
  SUBI r4, 1
  JNZ 十進吐き
  POP r5
  POP r4
  RET

; ---- 帯描き：見せの 16 桁を 2 進の帯に。左端が最上位 ----
; 帯ひとつは横 8 画素（語 2 つ）×縦 24 段。立った桁は刈安、
; 寝た桁は鈍色。語単位の ST なので、毎フレーム描いても軽い。
帯描き:
  PUSH r4
  PUSH r5
  PUSH r6
  LDI r3, 0
  LD r6, [r3+見せ]       ; r6 = 帯にする数
  LDI r5, 36             ; y は 36..59
帯段:
  LDI r4, 0              ; r4 = 桁の番号（0 が最上位）
帯桁:
  LDI r0, 15
  SUB r0, r4
  MOV r1, r6
  SHR r1, r0
  LDI r0, 1
  AND r1, r0             ; この桁は立っているか
  LDI r2, 2              ; 鈍色 — 寝ている
  CMPI r1, 0
  JZ 帯色よし
  LDI r2, 12             ; 刈安 — 立っている
帯色よし:
  LDI r3, 0x1111
  MUL r2, r3             ; 同じ色 4 画素ぶんの語
  MOV r0, r5
  LDI r1, 5
  SHL r0, r1             ; y*32
  ADD r0, r4
  ADD r0, r4             ; + 桁*2 語
  ADDI r0, 画面
  ST [r0], r2
  ST [r0+1], r2
  ADDI r4, 1
  CMPI r4, 16
  JNZ 帯桁
  ADDI r5, 1
  CMPI r5, 60
  JNZ 帯段
  POP r6
  POP r5
  POP r4
  RET

; ---- 見せる：FLIP に書き、ホストが下ろすまで待つ ----
見せる:
  LDI r0, 見せろ
  LDI r1, 1
  ST [r0], r1
見せ待ち:
  LD r1, [r0]
  CMPI r1, 0
  JNZ 見せ待ち
  RET

; ---- 計算係の記憶 ----
累積: .word 0            ; ここまでの答え
入力: .word 0            ; いま打っている数
演算: .word 0            ; 約束している演算子（0 なら無）
見せ: .word 0            ; 帯にして見せる数
