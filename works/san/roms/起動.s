; ============================================================
; 起動の画 — 電源を入れたときの、この機械の顔。
;
; なにか：上半分に 16 色のパレットがそのまま帯になって流れ、
;         下半分では市松紋様がゆっくり明滅しながら歩いてゆく。
;         遊ぶものではない。電源の入った計算機が「生きている」と
;         告げるためだけの画である。
; あそびかた：見る。それだけ。帯は 8 フレームでひと色ぶん流れ、
;         市松は 4 フレームでひと升ぶん歩く。永遠に。
;
; 描き方の工夫：1 ワードに 4 画素が入るので、色 c を 0x1111 倍
; すると「同じ色 4 つ」の語ができる。これをワード単位の ST で
; 置いてゆけば、画素を 1 つずつ打つより 4 倍はやい。
; ============================================================

.const 画面, 0x8000      ; VRAM の先頭
.const 見せろ, 0xF000    ; FLIP — ここに書くと画面に出る
.const 時, 0xF001        ; FRAME — 電源投入からのフレーム数

主:
  ; いまの時を読み、帯の位相を決める（8 フレームでひと色）
  LDI r0, 時
  LD r7, [r0]            ; r7 = いまのフレーム
  MOV r6, r7
  LDI r0, 3
  SHR r6, r0             ; r6 = 帯の位相

  LDI r5, 0              ; r5 = y（画面の段、0..95）
縦:
  LDI r4, 0              ; r4 = 語の x（0..31。1 語 = 4 画素）
横:
  ; この語の番地 = 画面 + y*32 + 語x
  MOV r0, r5
  LDI r1, 5
  SHL r0, r1             ; y*32
  ADD r0, r4
  ADDI r0, 画面          ; r0 = 番地

  CMPI r5, 48
  JLT 帯                 ; 上半分（y<48）は 16 色の帯

  ; ---- 下半分：市松。((y>>3) + 語x + (時>>2)) の偶奇で二色 ----
  MOV r1, r5
  LDI r2, 3
  SHR r1, r2             ; y を 8 段ずつの升に
  ADD r1, r4
  MOV r2, r7
  LDI r3, 2
  SHR r2, r3             ; 時の歩み（4 フレームでひと升）
  ADD r1, r2
  LDI r2, 1
  AND r1, r2             ; 偶奇 → 0 か 1
  LDI r2, 6
  MUL r1, r2
  ADDI r1, 8             ; 0 → 8（藍）、1 → 14（紫根）
  JMP 塗る

帯:
  ; ---- 上半分：語 2 つ（8 画素）ごとにひと色、位相ぶんずらす ----
  MOV r1, r4
  LDI r2, 1
  SHR r1, r2             ; 語x>>1 = 帯の番号（0..15）
  ADD r1, r6             ; 位相を足して流す
  LDI r2, 15
  AND r1, r2             ; 16 色を巡る

塗る:
  LDI r2, 0x1111
  MUL r1, r2             ; 同じ色 4 画素ぶんの語にする
  ST [r0], r1
  ADDI r4, 1
  CMPI r4, 32
  JNZ 横
  ADDI r5, 1
  CMPI r5, 96
  JNZ 縦

  CALL 見せる
  JMP 主                 ; 永遠に

; ---- 見せる：FLIP に書き、ホストが下ろすまで待つ ----
見せる:
  LDI r0, 見せろ
  LDI r1, 1
  ST [r0], r1
見せ待ち:
  LD r1, [r0]
  CMPI r1, 0
  JNZ 見せ待ち
  RET
