/* ============================================================
   SHA-256 — 依存ゼロの実装（FIPS 180-4）。

   くじが「誰にも操作できない」と言い切るには、種から結果への変換が
   逆算も細工もできない一方向写像でなければならない。だからおもちゃの
   ハッシュではなく、世界中が検証してきた SHA-256 を手づくりで積む。
   既知のテストベクトルと一字一句照合できる（tests/sha256.test.js）。
   ============================================================ */

